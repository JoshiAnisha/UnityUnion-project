            <section id="steps">
                <h2>Step-by-Step Process</h2>
                <ol>
                    <li><strong>Sign Up:</strong> Register with your basic information (name, email, and password).</li>
                    <li><strong>Complete Your Profile:</strong> Add details like skills, interests, and availability, and upload a photo.</li>
                    <li><strong>Submit Documentation:</strong> Submit necessary documents for a background check or parental consent if required.</li>
                    <li><strong>Browse Opportunities:</strong> Search and apply for volunteer opportunities.</li>
                    <li><strong>Attend Orientation:</strong> Attend any required training or orientation sessions.</li>
                    <li><strong>Receive Confirmation:</strong> Get confirmation with event details.</li>
                    <li><strong>Participate:</strong> Arrive on time and engage fully in the event.</li>
                    <li><strong>Feedback and Reflection:</strong> Provide and receive feedback to improve the experience.</li>
                </ol>
            </section>
