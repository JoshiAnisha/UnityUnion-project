                <h2>Criteria for Becoming a Volunteer</h2>
                <ul>
                    <li>Minimum Age: Volunteers must be at least 18 years old.</li>
                    <li>Commitment: Be willing to commit to a minimum of 5 hours per month.</li>
                    <li>Basic Skills: Good communication skills and the ability to work as part of a team.</li>
                    <li>Background Check: Required for certain positions.</li>
                    <li>Availability: Flexibility to attend training sessions and scheduled volunteer events.</li>
                    <li>Positive Attitude: A friendly, reliable, and proactive attitude.</li>
                </ul>
