            <section id="steps">
                <h2>Step-by-Step Process</h2>
                <ol>
                    <li><strong>Sign Up:</strong> Register with your organization's basic information (name, email, and password).</li>
                    <li><strong>Complete Your Profile:</strong> Add details such as mission statement, contact details, and website.</li>
                    <li><strong>Verification:</strong> Provide necessary documents to verify your organization's authenticity (e.g., registration certificate).</li>
                    <li><strong>Post Volunteer Opportunities:</strong> Create listings with details like date, time, location, skills required, and number of volunteers needed.</li>
                    <li><strong>Review Applications:</strong> Manage applications from volunteers and communicate with them as needed.</li>
                    <li><strong>Confirmation:</strong> Confirm selected volunteers and provide event details and instructions.</li>
                    <li><strong>Engage Volunteers:</strong> Ensure smooth execution of volunteer events and provide necessary support and guidance.</li>
                </ol>
            </section>
